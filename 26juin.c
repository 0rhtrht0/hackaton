int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

void    mena(char *c, int j)
{
    int i;

    i = 0;
    while (i <= j)
    {
        printf("\033[1;31m%s\033[0m", c);
        i++;
    }
}

void    maintso(char *c, int j)
{
    int i;

    i = 0;
    while (i <= j)
    {
        printf("\033[1;32m%s\033[0m", c);
        i++;
    }
}

void    fotsy(char *c, int j)
{
    int i;
    int k;

    i = 0;
    k = 30;
    while(k--)
        write(1, " ", 1);
    while (i <= j)
    {
        printf("%s", c);
        i++;
    }
}

void    tsipika(char *s, int j, int o)
{
    int i;

    i = j / 3;
    if (o == 0)
    {
        fotsy(s, i + 3);
        i = j - i;
        mena(s, j);
    }
    if (o == 1)
    {
        fotsy(s, i + 3);
        i = j - i;
        maintso(s, j);
    }
}

void    printLine(const char *tonony) 
{
    int k;

    k = 50;
    while(k--)
        write(1, " ", 1);
    printf("\033[1;37m%s\033[0m\n", tonony);
    fflush(stdout);
    sleep(4);
    usleep(500000);
}

void    saina (char *s, int halavany, int  sakany)
{
    int i;

    i = 0;
    while (i <= halavany)
    {
        tsipika(s, sakany, 0);
        usleep(80000);
        printf("\n");
        i++;
    }
    i = 0;
    while (i <= halavany)
    {
        tsipika(s, sakany, 1);
        usleep(80000);
        printf("\n");
        i++;
    }
    sleep(10);
}

